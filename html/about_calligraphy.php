        <h2>How did it all start? </h2>
        <br>
        <p>
        In late 2017, the idea to launch my website came to me. While I loved working with clients, I felt like something was missing. I was completely immersed in the world of lettering and calligraphy – it was the only thing I could think about and the only thing I wanted to do. Initially, my idea was to curate an Instagram page featuring talented and hardworking artists that inspired me. As the community grew, I received an increasing number of questions about getting started and recommended tools. That’s when I decided to start sharing my knowledge and experiences in the form of tutorials and articles on a website. Over the years, my website has evolved into more than just a passion project, and I am incredibly grateful for everything it has become.
